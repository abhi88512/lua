-- ============================================
-- 01: COMMENTS IN LUA
-- ============================================
-- Comments are like sticky notes for your code
-- They help you (and others) understand what's happening
-- Lua ignores everything in comments

-- Single line comment
-- This is a comment that starts with two dashes

--[[
    Multi-line comment
    You can write as much as you want here
    Just wrap it in --[[ and --]]
    
    This is useful for:
    - Explaining complex code
    - Temporarily disabling code
    - Leaving notes for future you (who will thank past you)
--]]

--[[
    NESTED COMMENT EXAMPLE (Rarely used, but good to know)
    If you need to comment out code that already has multi-line comments,
    you can use --[=[ and ]=]-- for nested comments
    But honestly, you probably won't need this unless you're doing something fancy
--]]

-- Quick tip: In most editors, you can comment/uncomment code with Ctrl+/ (or Cmd+/ on Mac)
-- But manual commenting is good for learning!

