-- ============================================
-- BOOLEANS
-- ============================================
local truth = true
local lies = false

print("Truth:", truth)
print("Lies:", lies)

-- ============================================
-- NIL (The "Nothing" Value)
-- ============================================
local nothing = nil  -- nil means "no value" / "empty" / "doesn't exist"

print("Nothing:", nothing)  -- Prints: nil

-- nil is special - it's the only "falsy" value besides false
-- More on this in the conditionals file!

-- nil is used to represent:
-- - Missing values
-- - Uninitialized variables
-- - Removed table entries

